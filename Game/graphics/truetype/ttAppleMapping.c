static StashTable appleToUnicodeHashMap;
static StashTable unicodeToAppleHashMap;

void initAppleAndUnicodeMap(){
	if(!appleToUnicodeHashMap){
		int i;
		appleToUnicodeHashMap = stashTableCreate(ARRAY_SIZE(AppleToUnicodeMap), StashDefault, StashKeyTypeInts, sizeof(unsigned short));
		for(i = 0; i < ARRAY_SIZE(AppleToUnicodeMap); i++){	
			stashIntAddInt(appleToUnicodeHashMap, AppleToUnicodeMap[i].mappedCharacter, AppleToUnicodeMap[i].unicode, false);
		}
	}

	if(!unicodeToAppleHashMap){
		int i;
		unicodeToAppleHashMap = stashTableCreate(ARRAY_SIZE(AppleToUnicodeMap), StashDefault, StashKeyTypeInts, sizeof(unsigned short));
		for(i = 0; i < ARRAY_SIZE(AppleToUnicodeMap); i++){	
			stashIntAddInt(unicodeToAppleHashMap, AppleToUnicodeMap[i].unicode, AppleToUnicodeMap[i].mappedCharacter, false);
		}
	}
}

unsigned short appleToUnicode(char appleCharacter){
	int iResult;
	if(!appleToUnicodeHashMap)
		initAppleAndUnicodeMap();
	if ( stashIntFindInt(appleToUnicodeHashMap, appleCharacter, &iResult))
		return iResult;
	return 0;
}

char unicodeToApple(unsigned short unicodeCharacter){
	int iResult;
	if(!unicodeToAppleHashMap)
		initAppleAndUnicodeMap();
	if ( stashIntFindInt(unicodeToAppleHashMap, unicodeCharacter, &iResult))
		return iResult;
	return 0;
}
