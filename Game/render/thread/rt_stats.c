static int rt_stats_cards_wanted = 0;
static int rt_stats_frame_id = 0;

void rdrStatsFrameStart()
{
    ++rt_stats_frame_id;
}

void rdrStatsClear(int cards_wanted)
{
    rt_stats_cards_wanted = cards_wanted;

    memset(&rt_stats,0,sizeof(rt_stats));
    rt_stats.frame_id = rt_stats_frame_id;

    //start timing render command issued
    rt_stats.cycles_cpu = timerCpuTicks64();
}

void rdrStatsGet(RTStats *dest)
{
    //stop timing render command issued
    rt_stats.cycles_cpu = timerCpuTicks64() - rt_stats.cycles_cpu;

	memcpy(dest, &rt_stats, sizeof(rt_stats));
}


void rdrStatFinishModel(int triCount)
{
	int i, j, card, prevX, curX, texchanges;
	float prevY, cost;

	int column = rt_stats.curmodel_changes & (STATS_CHANGE_BUFFER | STATS_CHANGE_BLENDMODE | STATS_CHANGE_VP);
	int column_prevtex = column;

	texchanges = 0;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE0)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE1)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE2)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE3)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE4)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE5)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE6)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE7)
		texchanges++;
	if (rt_stats.curmodel_changes & STATS_CHANGE_TEXTURE8)
		texchanges++;

    devassertmsg(texchanges < MAX_TEX_BINDS, "Array bounds exceeded");
	RT_STAT_INC(drawcall_texbinds[texchanges]);

	if (texchanges > 3)
	{
		column |= 3 << 3;
		column_prevtex |= 2 << 3;
	}
	else
	{
		column = column_prevtex = column | (texchanges << 3);
	}
	
	if (!rt_stats_cards_wanted)
		return;

	// for each card we care about, record the cost of this model
	for (i = 0; i < STATS_NUM_CARDS; i++)
	{
		float *row;
		cost = 0;

		card = 1 << i;
		if (!(rt_stats_cards_wanted & card))
			continue;

		row = stats_CardInfo[i].times[column];
		prevX = 0;
		if (triCount < stats_CardInfo[i].samplePoints[0])
		{
			// prevY is the y-intercept of the line from sample point 0 to sample point 1
			prevY = row[0] - (((float)stats_CardInfo[i].samplePoints[0]) * (row[1] - row[0]) / ((float)(stats_CardInfo[i].samplePoints[1] - stats_CardInfo[i].samplePoints[0])));
			if (prevY < 0)
				prevY = 0;
		}

		for (j = 0; j < STATS_TABLE_SAMPLE_POINTS && (curX = stats_CardInfo[i].samplePoints[j]) >= 0; j++)
		{
			if (triCount < curX)
			{
				float percent = ((float)(triCount - prevX)) / ((float)(curX - prevX));
				cost = prevY * (1.f - percent) + row[j] * percent;
				break;
			}

			prevX = stats_CardInfo[i].samplePoints[j];
			prevY = row[j];
		}

		if (j == STATS_TABLE_SAMPLE_POINTS || curX < 0)
		{
			// interpolate out from last two sample points
			j -= 2;
			if (j > 0)
			{
				float m = (row[j+1] - row[j]) / ((float)(stats_CardInfo[i].samplePoints[j+1] - stats_CardInfo[i].samplePoints[j]));
				float b = row[j] - (((float)stats_CardInfo[i].samplePoints[j]) * m);
				float y = m * triCount + b;
				if (y > 0)
					cost = y;
			}
		}

		if (cost > 0)
			rt_stats.costs[i] += cost;
	}
}

